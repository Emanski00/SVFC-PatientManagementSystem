    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        String id = medId.getText();
        String name = medName.getText();
        String brand = medBrand.getText();
        Calendar expDate = null;
        String status = statusBox.getSelectedItem().toString();
        String guides = medGuide.getText();
        long quantity = 0;
        if (id.isEmpty() || name.isEmpty() || brand.isEmpty() || (expDate = this.expDate.getCalendar()) == null
                || (medQuan.getText()).isEmpty() || status.isEmpty() || guides.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill all the textboxes!", "Empty Fields", JOptionPane.INFORMATION_MESSAGE);

        } else {
            quantity = Long.parseLong(medQuan.getText());

            Medicines medicine = new Medicines();
            medicine.setId(id);
            medicine.setName(name);
            medicine.setBrand(brand);
            medicine.setQuantity(quantity);

            medicine.setExpDate(expDate.getTime().toString());
            medicine.setStatus(status);
            medicine.setGuides(guides);
            MedicineServiceImpl medicineServiceImpl = new MedicineServiceImpl();

            if (addBtn.getText().toLowerCase().equals("update")) {
                medicine.setCount(count);
                if (medicineServiceImpl.updateMedicineData(count, medicine)) {
                    JOptionPane.showMessageDialog(null, "Medicine Data Updated");
                    this.dispose();
                }
            } else {
                if (medicineServiceImpl.insertMedicine(medicine)) {
                    JOptionPane.showMessageDialog(null, "Medicine Data Inserted");
                    this.dispose();
                }
            }
        }


    }//GEN-LAST:event_addBtnActionPerformed

    private void medQuanKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_medQuanKeyPressed
        char c = evt.getKeyChar();
        if (Character.isDigit(c)) {
            // If the character is a digit, keep it
            medQuan.setEditable(true);
        } else if (Character.isISOControl(c)) {
            // If the character is a control character (e.g. backspace, delete), keep it
            medQuan.setEditable(true);
        } else {
            // Otherwise, remove the character
            medQuan.setEditable(false);
        }
    }//GEN-LAST:event_medQuanKeyPressed

