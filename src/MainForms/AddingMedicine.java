    public AddingMedicine(java.awt.Frame parent, boolean modal, long id) {
        super(parent, modal);
        initComponents();
        this.setLocationRelativeTo(null);

        count = id;

        if (id >= 1) {
            factory = new Configuration()
                    .configure("/DatabaseSettings/hibernate.cfg.xml")
                    .addAnnotatedClass(Medicines.class)
                    .buildSessionFactory();
            Session session = factory.openSession();
            CriteriaBuilder builder = session.getCriteriaBuilder();
            CriteriaQuery<Medicines> query = builder.createQuery(Medicines.class);
            Root<Medicines> root = query.from(Medicines.class);

            Predicate predicate = builder.equal(root.get("count"), id);
            query.where(predicate);

            List<Medicines> results = session.createQuery(query).getResultList();

            for (Medicines medicine : results) {
                count = medicine.getCount();

                medId.setText(medicine.getId());
                String dateValue = medicine.getExpDate();
                DateFormat format = new SimpleDateFormat("EEE MMM dd HH:mm:ss zzz yyyy");
                Date date;
                try {
                    date = format.parse(dateValue);
                    expDate.setDate(date);
                } catch (ParseException ex) {
                    Logger.getLogger(AddingPatient.class.getName()).log(Level.SEVERE, null, ex);
                }

                medName.setText(medicine.getName());
                medBrand.setText(medicine.getBrand());
                medQuan.setText(Long.toString(medicine.getQuantity()));

                statusBox.setSelectedItem(medicine.getStatus());
                medGuide.setText(medicine.getGuides());

            }
            addBtn.setText("Update");

            session.close();

        }
    }

